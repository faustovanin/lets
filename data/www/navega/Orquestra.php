<h1>Orquestra</h1>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><span style='mso-tab-count:1'>            </span>Uma
orquestra é um agrupamento instrumental utilizado<span class=GramE> sobretudo</span>
para a execução de música erudita. <span class=GramE>A pequenas orquestras
dá-se</span> o nome de orquestras de câmara<i style='mso-bidi-font-style:normal'>.<o:p></o:p></i></p>

<p class=MsoNormal><i style='mso-bidi-font-style:normal'><span
style='mso-tab-count:1'>            </span></i><span class=GramE>A orquestras
completas dá-se</span> o nome de orquestras sinfônicas ou orquestras filarmônicas;
embora estes prefixos não especifiquem nenhuma diferença no que toca à
constituição instrumental ou ao papel da mesma, podem revelar-se úteis para
distinguir orquestras de uma mesma localidade. Na verdade, esses prefixos
denotam a maneira que é sustentada a orquestra. A orquestra filarmônica é
sustentada por uma instituição privada, ficando assim a sinfônica mantida por
uma instituição pública. Uma orquestra terá, tipicamente, mais de oitenta
músicos, em alguns casos mais de cem, embora em atuação esse número seja
ajustado em função da obra reproduzida. Em alguns casos, uma orquestra pode
incluir músicos contratados para tocar instrumentos específicos que não compõem
o conjunto oficial: por exemplo, nem todas as orquestras têm um harpista ou um
saxofonista.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Uma orquestra sinfônica dispõe cinco classes de
instrumentos:</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:18.0pt;text-indent:-18.0pt;mso-list:l0 level1 lfo2;
tab-stops:list 18.0pt'><![if !supportLists]><span style='font-family:Symbol;
mso-fareast-font-family:Symbol;mso-bidi-font-family:Symbol'><span
style='mso-list:Ignore'>¨<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span class=GramE>as</span> cordas (violinos,
violas, violoncelos, contrabaixos, harpas)</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:18.0pt;text-indent:-18.0pt;mso-list:l0 level1 lfo2;
tab-stops:list 18.0pt'><![if !supportLists]><span style='font-family:Symbol;
mso-fareast-font-family:Symbol;mso-bidi-font-family:Symbol'><span
style='mso-list:Ignore'>¨<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span class=GramE>as</span> madeiras (flautas,
flautins, oboés, <span class=SpellE>corne-inglês</span>, clarinetes, clarinete
baixo, fagotes, <span class=SpellE>contrafagotes</span>)</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:18.0pt;text-indent:-18.0pt;mso-list:l0 level1 lfo2;
tab-stops:list 18.0pt'><![if !supportLists]><span style='font-family:Symbol;
mso-fareast-font-family:Symbol;mso-bidi-font-family:Symbol'><span
style='mso-list:Ignore'>¨<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span class=GramE>os</span> metais (trompetes,
trombones, trompas, tubas)</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-left:18.0pt;text-indent:-18.0pt;mso-list:l0 level1 lfo2;
tab-stops:list 18.0pt'><![if !supportLists]><span style='font-family:Symbol;
mso-fareast-font-family:Symbol;mso-bidi-font-family:Symbol'><span
style='mso-list:Ignore'>¨<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span class=GramE>os</span> instrumentos de
percussão (tímpanos, triângulo, caixas, bumbo, pratos, carrilhão <span
class=SpellE>sinfónico</span>, etc.)</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal style='margin-top:5.0pt;margin-right:0cm;margin-bottom:5.0pt;
margin-left:18.0pt;text-align:justify;text-indent:-18.0pt;mso-list:l2 level1 lfo3;
tab-stops:list 18.0pt'><![if !supportLists]><span style='font-family:Symbol;
mso-fareast-font-family:Symbol;mso-bidi-font-family:Symbol'><span
style='mso-list:Ignore'>¨<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
</span></span></span><![endif]><span class=GramE>os</span> instrumentos de
teclas (piano, cravo, órgão)</p>

<p style='text-align:justify;text-indent:35.4pt'>Entre estes grupos de
instrumentos e em cada um deles existe uma hierarquia implicitamente aceita.
Cada seção (ou grupo de instrumentos) provê um <a
href="http://pt.wikipedia.org/wiki/Solo_%28m%C3%BAsica%29" title="Solo (música)"><span
style='color:windowtext;text-decoration:none;text-underline:none'>solista</span></a>
(ou principal) que será o protagonista dos solos e da liderança do grupo. Os
violinos são divididos em dois grupos: primeiros violinos e segundos violinos 
o que pressupõe dois principais. O principal dos primeiros violinos é designado
como chefe não só de toda a seção de cordas<span class=GramE> mas</span> de
toda a orquestra, subordinado unicamente ao <a
href="http://pt.wikipedia.org/wiki/Maestro" title=Maestro><span
style='color:windowtext;text-decoration:none;text-underline:none'>maestro</span></a>,
esse violinista é denominado <i style='mso-bidi-font-style:normal'>spalla</i>.
Nos metais, o primeiro trombonista é o líder, enquanto que nas madeiras esse
papel cabe ao primeiro oboísta.</p>

<p style='text-align:justify;text-indent:35.4pt'>Atualmente, as orquestras são
conduzidas por um maestro, embora não fosse assim com as orquestras originais,
sendo a condução responsabilidade do líder de orquestra. Também em outros casos
não existe maestro: em orquestras pequenas, ou em reproduções realistas de <a
href="http://pt.wikipedia.org/wiki/M%C3%BAsica_barroca" title="Música barroca"><span
style='color:windowtext;text-decoration:none;text-underline:none'>música
barroca</span></a>.</p>

<p class=MsoNormal style='text-align:justify'><b style='mso-bidi-font-weight:
normal'><o:p>&nbsp;</o:p></b></p>

<h1>Etimologia</h1>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p style='text-align:justify;text-indent:35.4pt'>Esta palavra designa não só um
grupo de músicos que interpretam obras musicais com diversos instrumentos como
também uma parte do <a href="http://pt.wikipedia.org/wiki/Teatro_grego"
title="Teatro grego"><span style='color:windowtext;text-decoration:none;
text-underline:none'>teatro grego</span></a>, que se caracterizava por um coro
formado por bailarinos e músicos que faziam evoluções sobre um estrado chamado
'<span class=SpellE>orkhéstra</span>', situado entre o cenário e os
espectadores.</p>

<p style='text-align:justify;text-indent:35.4pt'>'<span class=SpellE>Orkhéstra</span>'
provinha do verbo '<span class=SpellE>orcheisthai</span>', que significava
'dançar' ou 'eu danço'. O vocábulo grego passou ao latim como '<span
class=SpellE>orchestra</span>', com o mesmo significado, como documentam os
escritos de diversos poetas romanos. No século I, <span class=SpellE>Vitruvio</span>
e <span class=SpellE>Suetonio</span> a utilizaram para designar o lugar
destinado aos senadores no teatro romano. A palavra chegou ao francês em fins
da <a href="http://pt.wikipedia.org/wiki/Idade_M%C3%A9dia" title="Idade Média"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Idade Média</span></a>,
em traduções de <span class=SpellE>Suetonio</span>, porém só se aplicou ao
teatro moderno a partir do século XVIII, com a ópera italiana.</p>

<h3 style='text-align:justify'><span class=mw-headline>A orquestra barroca</span></h3>

<p style='text-align:justify;text-indent:35.4pt'>A orquestra barroca era
largamente baseada nos instrumentos da <a
href="http://pt.wikipedia.org/w/index.php?title=Fam%C3%ADlia_das_violas&amp;action=edit&amp;redlink=1"
title="Família das violas (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>família das violas</span></a>. Assim
era a orquestra usada por <span class=SpellE>Monteverdi</span>, e esta família
de instrumentos predominou por todo o século XVII. Uma boa forma de conhecer
estes instrumentos é através do filme <i style='mso-bidi-font-style:normal'>Todas
as manhãs do mundo</i>, estrelado por <a
href="http://pt.wikipedia.org/wiki/Gerard_Depardieu" title="Gerard Depardieu"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Gerard
Depardieu</span></a>. Neste filme o ator representa <span class=GramE>o
violista e compositor Marin Marais (1656-1728)</span>, um dos músicos mais
célebres do século XVII. As dublagens não são muito convincentes quanto aos
movimentos dos executantes da viola da gamba, mas a trilha sonora original é de
responsabilidade de <a href="http://pt.wikipedia.org/wiki/Jordi_Savall"
title="Jordi Savall"><span class=SpellE><span style='color:windowtext;
text-decoration:none;text-underline:none'>Jordi</span></span><span
style='color:windowtext;text-decoration:none;text-underline:none'> <span
class=SpellE>Savall</span></span></a> a grande autoridade do instrumento na
atualidade.</p>

<p style='text-align:justify;text-indent:35.4pt'>Ao final do período barroco os
instrumentos da família das violas foram perdendo a preferência para os da <a
href="http://pt.wikipedia.org/w/index.php?title=Fam%C3%ADlia_dos_violinos&amp;action=edit&amp;redlink=1"
title="Família dos violinos (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>família dos violinos</span></a>.
Assim, as orquestras do início do século XVIII já eram basicamente formadas por
um padrão de instrumentos de cordas de arco, que continuam sendo <span
class=GramE>a</span> base das orquestras até hoje. Dois compositores podem ser
destacados como pioneiros na escrita para <b style='mso-bidi-font-weight:normal'><i
style='mso-bidi-font-style:normal'>cordas</i></b>: <a
href="http://pt.wikipedia.org/w/index.php?title=Corelli&amp;action=edit&amp;redlink=1"
title="Corelli (página não existe)"><span class=SpellE><span style='color:windowtext;
text-decoration:none;text-underline:none'>Corelli</span></span></a> (1623-1713)
e <a href="http://pt.wikipedia.org/wiki/Vivaldi" title=Vivaldi><span
style='color:windowtext;text-decoration:none;text-underline:none'>Vivaldi</span></a>
(1678-1741). Outros compositores do período cujas obras instrumentais também já
são largamente baseadas nas cordas da família dos violinos: J. S. <a
href="http://pt.wikipedia.org/wiki/Bach" title=Bach><span style='color:windowtext;
text-decoration:none;text-underline:none'>Bach</span></a> (1685-1750), <a
href="http://pt.wikipedia.org/wiki/Handel" title=Handel><span style='color:
windowtext;text-decoration:none;text-underline:none'>Handel</span></a>
(1685-1759) e <a href="http://pt.wikipedia.org/wiki/Telemann" title=Telemann><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Telemann</span></span></a> (1681-1767).</p>

<p style='text-align:justify;text-indent:35.4pt'>A orquestra no período barroco
não estava ainda fixada como formação instrumental, pois havia grande variação
entre as formações empregadas por cada compositor, ou mesmo entre obras
diferentes do mesmo compositor. Mas já se podem ver as tendências que se
afirmariam plenamente no <a
href="http://pt.wikipedia.org/wiki/Per%C3%ADodo_cl%C3%A1ssico"
title="Período clássico"><span style='color:windowtext;text-decoration:none;
text-underline:none'>período clássico</span></a>: substituição dos instrumentos
da família das violas pelos da família dos violinos; abandono da grande
variedade de instrumentos antigos de sopro; prática de escrever para as cordas
a quatro partes, como se fossem as quatro vozes de um <a
href="http://pt.wikipedia.org/wiki/Coral" title=Coral><span style='color:windowtext;
text-decoration:none;text-underline:none'>coral</span></a>. Não se pode deixar
de mencionar outros dois importantes fatores. O fim do período barroco coincide
também com uma grande mudança na construção dos instrumentos musicais, devido à
novidade da afinação pelo <b style='mso-bidi-font-weight:normal'><i
style='mso-bidi-font-style:normal'>sistema</i></b> <b style='mso-bidi-font-weight:
normal'><i style='mso-bidi-font-style:normal'>temperado</i></b>, que passava a
substituir o antigo sistema de afinação, por <span class=GramE>oposição
denominado como <b style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:
normal'>não-temperado</i></b></span>. </p>

<p style='text-align:justify;text-indent:35.4pt'>Outro fator é que toda a
música instrumental do período barroco foi fortemente marcada pela tradição do <a
href="http://pt.wikipedia.org/wiki/Baixo_cont%C3%ADnuo" title="Baixo contínuo"><span
style='color:windowtext;text-decoration:none;text-underline:none'>baixo
contínuo</span></a>, forma de escrita e de execução em que apenas a linha do
baixo é definida na partitura, ficando toda a execução da harmonia a critério
do músico que tocava os instrumentos <b style='mso-bidi-font-weight:normal'>harmônicos</b>.
Esta tradição seria abandonada com o fim do período barroco.</p>

<h3 style='text-align:justify'><span class=mw-headline>A orquestra clássica</span></h3>

<p style='text-align:justify;text-indent:35.4pt'>Foi no período conhecido como <a
href="http://pt.wikipedia.org/wiki/Classicismo" title=Classicismo><span
style='color:windowtext;text-decoration:none;text-underline:none'>classicismo</span></a>
que a orquestra tomou sua formação atual, simultaneamente ao surgimento da
idéia de <a
href="http://pt.wikipedia.org/w/index.php?title=M%C3%BAsica_absoluta&amp;action=edit&amp;redlink=1"
title="Música absoluta (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>música absoluta</span></a> que se
tornou critério positivo de valor estético. Isto não teria ocorrido sem o
desenvolvimento da <a href="http://pt.wikipedia.org/wiki/Forma-sonata"
title=Forma-sonata><span style='color:windowtext;text-decoration:none;
text-underline:none'>forma-sonata</span></a> e dos gêneros da <a
href="http://pt.wikipedia.org/wiki/Sinfonia" title=Sinfonia><span
style='color:windowtext;text-decoration:none;text-underline:none'>sinfonia</span></a>,
do <a href="http://pt.wikipedia.org/wiki/Quarteto_de_cordas"
title="Quarteto de cordas"><span style='color:windowtext;text-decoration:none;
text-underline:none'>quarteto de cordas</span></a>, da <a
href="http://pt.wikipedia.org/wiki/Sonata" title=Sonata><span style='color:
windowtext;text-decoration:none;text-underline:none'>sonata</span></a> e do <a
href="http://pt.wikipedia.org/wiki/Concerto" title=Concerto><span
style='color:windowtext;text-decoration:none;text-underline:none'>concerto</span></a>.
Apesar dos termos sinfonia, sonata e concerto já existirem no período barroco,
foi no fim do século XVIII que eles assumiram um significado mais preciso em
termos de forma musical. E foram estas formas clássicas que deram tanta
importância à orquestra sinfônica.</p>

<p style='text-align:justify;text-indent:35.4pt'>Isto coincidiu com mudanças na
construção dos instrumentos e na própria maneira de tocar dos conjuntos
orquestrais. Estes ganharam em equilíbrio, afinação, precisão e,
principalmente, variação de dinâmica e articulação. A orquestra pioneira desta
transformação foi <span class=GramE>a</span> orquestra de Mannheim, sob a
direção do violinista e compositor Johann Stamitz (1717-1757) a partir de 1745.</p>

<p style='text-align:justify;text-indent:35.4pt'>Stamitz foi um dos
responsáveis pelo desenvolvimento da forma-sonata e da sinfonia, bem como é
apontado como o responsável pelo alto nível técnico de execução atingido por
esta orquestra. Devido ao seu padrão de excelência técnica e à novidade das
obras musicais executadas em Mannheim, esta orquestra tornou-se modelo para os
compositores normalmente reconhecidos como os mestres do período clássico: <a
href="http://pt.wikipedia.org/wiki/Mozart" title=Mozart><span style='color:
windowtext;text-decoration:none;text-underline:none'>Mozart</span></a>, Haydn e
Beethoven. São estes também conhecidos como os principais compositores de
sinfonias, responsáveis pela definição moderna do gênero e, com ele, pela
definição do que pode ser chamado de orquestra clássica.</p>

<p style='text-align:justify;text-indent:35.4pt'>Esta seria uma orquestra
baseada nos instrumentos de cordas de arco, tratados pelos compositores como
padrão para o qual se escreve a quatro partes, como já vinha se <span
class=SpellE>tradicionalizando</span> desde o início do século XVIII. Além
disso, esta orquestra clássica era normalmente acrescida das madeiras a dois
(duas flautas, dois oboés, dois clarinetes e dois fagotes) e das trompas,
ocasionalmente tímpanos, trompetes e trombones. Neste período até cerca de 1820
os instrumentos de boca, ou também classificados como padrão dos metais, ainda
eram muito limitados pela inexistência de válvulas ou pistões. Assim tinham
dificuldades com <b style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:
normal'>modulações</i></b> e <b style='mso-bidi-font-weight:normal'><i
style='mso-bidi-font-style:normal'>cromatismos</i>,</b> sendo praticamente
instrumentos que funcionavam em um único <b style='mso-bidi-font-weight:normal'><i
style='mso-bidi-font-style:normal'>tom principal</i></b> (e também em seus
vizinhos).</p>

<p style='text-align:justify;text-indent:35.4pt'><o:p>&nbsp;</o:p></p>

<h3 style='text-align:justify'><span class=mw-headline>A orquestra no século
XIX</span></h3>

<p style='text-align:justify;text-indent:35.4pt'>No século XIX, a orquestra
seguiu uma tendência de aumento na participação dos instrumentos de sopro.
Acredita-se que isso foi decorrência direta da Revolução Francesa, e da
conseqüente popularidade das fanfarras ou bandas militares. Assim, à orquestra
sinfônica incorporaram-se permanentemente os instrumentos do padrão dos metais,
com tendência a aumentar seu uso ao longo do século. A partir da década de 1820
estes instrumentos ganhariam estabilidade e versatilidade pela incorporação das
válvulas ou pistões, que permitiam que se tornassem instrumentos totalmente
cromáticos </p>

<p style='text-align:justify;text-indent:35.4pt'>Como são instrumentos de
grande potência sonora, o aumento no uso de instrumentos do padrão dos metais
levou ao aumento do tamanho da orquestra. Para manter o equilíbrio sonoro com
um crescente padrão de metais, as madeiras tiveram de sofrer considerável
aumento, chegando a ser comum o uso de madeiras a quatro. Neste caso, para não
ficar com a mesmice de quatro instrumentos iguais, cada um desenvolveu-se em
uma família própria. As flautas (<i style='mso-bidi-font-style:normal'><a
href="http://pt.wikipedia.org/w/index.php?title=Picollo&amp;action=edit&amp;redlink=1"
title="Picollo (página não existe)"><span class=SpellE><span style='color:windowtext;
text-decoration:none;text-underline:none'>picollo</span></span></a></i>, duas
flautas e flauta baixo, ou flauta em sol), os oboés (dois oboés, corne inglês e
<a
href="http://pt.wikipedia.org/w/index.php?title=Obo%C3%A9_d%E2%80%99amore&amp;action=edit&amp;redlink=1"
title="Oboé damore (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>oboé d<span class=SpellE>amore</span></span></a>),
os clarinetes (<a href="http://pt.wikipedia.org/wiki/Requinta" title=Requinta><span
style='color:windowtext;text-decoration:none;text-underline:none'>requinta</span></a>,
dois clarinetes e <a href="http://pt.wikipedia.org/wiki/Clarone" title=Clarone><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>clarone</span></span></a>, ou clarinete baixo) e os fagotes (três fagotes
e um <span class=SpellE>contrafagote</span>).</p>

<p style='text-align:justify;text-indent:35.4pt'>Este aumento em ambos os <span
class=SpellE>padrãos</span> de sopro levou à necessidade de uma quantidade
gigantesca de músicos no padrão das cordas, para que seu volume pudesse ser
equilibrado aos demais <span class=SpellE>padrãos</span> da orquestra, posto
que cada instrumento da família das cordas <span class=GramE>possui</span>
individualmente volume muito inferior aos instrumentos das madeiras e dos
metais.</p>

<p style='text-align:justify;text-indent:35.4pt'>Este aumento progressivo no
tamanho da orquestra levou a duas direções do ponto de vista da técnica musical
de <a href="http://pt.wikipedia.org/wiki/Orquestra%C3%A7%C3%A3o"
title=Orquestração><span style='color:windowtext;text-decoration:none;
text-underline:none'>orquestração</span></a>. Primeiro a orquestra tornou-se um
grupo de muito maior potência sonora, com isto também acompanhando a uma
tendência de aumento do tamanho das salas de concerto e de seu público. E em
segundo permitiu aos compositores uma gama muito maior de combinações de
timbres, o que numa comparação com as cores na pintura acabou recebendo a
qualificação de palheta orquestral.</p>

<p style='text-align:justify;text-indent:35.4pt'>Entre os compositores do
século XIX sempre houve aqueles que se mantiveram mais apegados à orquestra
clássica e às formas tradicionais da sinfonia e do concerto. Pode-se citar
neste grupo, sem a pretensão de ser exaustivo, compositores como <a
href="http://pt.wikipedia.org/wiki/Schubert" title=Schubert><span
style='color:windowtext;text-decoration:none;text-underline:none'>Schubert</span></a>,
<a href="http://pt.wikipedia.org/wiki/Schumann" title=Schumann><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Schumann</span></span></a>, <a
href="http://pt.wikipedia.org/wiki/Mendelssohn" title=Mendelssohn><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Mendelssohn</span></span></a>, <a
href="http://pt.wikipedia.org/wiki/Chopin" title=Chopin><span style='color:
windowtext;text-decoration:none;text-underline:none'>Chopin</span></a> ou <a
href="http://pt.wikipedia.org/wiki/Brahms" title=Brahms><span style='color:
windowtext;text-decoration:none;text-underline:none'>Brahms</span></a>. Outros
podem ser apontados como os mais comprometidos com o desenvolvimento da
orquestra e também, coincidentemente, das formas musicais (notadamente com a
criação do <a href="http://pt.wikipedia.org/wiki/Poema_sinf%C3%B4nico"
title="Poema sinfônico"><span style='color:windowtext;text-decoration:none;
text-underline:none'>poema sinfônico</span></a> e as novas formas de composição
de ópera) e da linguagem harmônica. São estes, principalmente, <a
href="http://pt.wikipedia.org/wiki/Berlioz" title=Berlioz><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Berlioz</span></span></a>,
<a href="http://pt.wikipedia.org/wiki/Liszt" title=Liszt><span
style='color:windowtext;text-decoration:none;text-underline:none'>Liszt</span></a>
e <a href="http://pt.wikipedia.org/wiki/Wagner" title=Wagner><span
style='color:windowtext;text-decoration:none;text-underline:none'>Wagner</span></a>,
numa tradição de vanguarda que continua na virada do século XIX para o XX com <a
href="http://pt.wikipedia.org/wiki/Bruckner" title=Bruckner><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Bruckner</span></span></a>,
<a href="http://pt.wikipedia.org/wiki/Mahler" title=Mahler><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Mahler</span></span></a>,
<a href="http://pt.wikipedia.org/wiki/Richard_Strauss" title="Richard Strauss"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Richard
Strauss</span></a>, <a href="http://pt.wikipedia.org/wiki/Rimsky-Korsakov"
title=Rimsky-Korsakov><span class=SpellE><span style='color:windowtext;
text-decoration:none;text-underline:none'>Rimsky-Korsakov</span></span></a>, <a
href="http://pt.wikipedia.org/w/index.php?title=Mussorgski&amp;action=edit&amp;redlink=1"
title="Mussorgski (página não existe)"><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Mussorgski</span></span></a>,
<a href="http://pt.wikipedia.org/wiki/Debussy" title=Debussy><span
style='color:windowtext;text-decoration:none;text-underline:none'>Debussy</span></a>,
<a href="http://pt.wikipedia.org/wiki/Ravel" title=Ravel><span
style='color:windowtext;text-decoration:none;text-underline:none'>Ravel</span></a>,
<a href="http://pt.wikipedia.org/wiki/Respighi" title=Respighi><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Respighi</span></span></a> e até o <a
href="http://pt.wikipedia.org/wiki/Schoenberg" title=Schoenberg><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Schoenberg</span></span></a> da primeira fase (antes de 1912).</p>

<p style='text-align:justify;text-indent:35.4pt'>São estes que desenvolvem
combinações inusitadas entre os timbres dos instrumentos, técnicas arrojadas de
execução dos próprios instrumentos bem como uma escrita rítmica e
harmonicamente cada vez mais complexa. A obra seminal é, sem dúvida, a <i
style='mso-bidi-font-style:normal'>Sinfonia Fantástica</i> de <span
class=SpellE>Berlioz</span>, escrita em 1830, apenas <span class=GramE>3</span>
anos após a morte de Beethoven. Outra obra considerada muito avançada foi o <i
style='mso-bidi-font-style:normal'>Prelúdio</i> da ópera <i style='mso-bidi-font-style:
normal'>Tristão e Isolda</i> da Wagner, concluída em 1859. Obra normalmente
lembrada por beirar o <a href="http://pt.wikipedia.org/wiki/Atonalismo"
title=Atonalismo><span class=SpellE><span style='color:windowtext;text-decoration:
none;text-underline:none'>atonalismo</span></span></a> ainda em plena metade do
século XIX, mas que também foi radical na escrita orquestral, bem como na
diluição da rítmica antes fundada em compassos e pulsos. Outra obra sempre
lembrada como paradigma orquestral é a <i style='mso-bidi-font-style:normal'>8ª
Sinfonia</i> (1907) de <span class=SpellE>Mahler</span>, apelidada de <i
style='mso-bidi-font-style:normal'>Sinfonia dos mil</i> pela grande quantidade
de músicos que emprega, entre orquestra e coral. (O número de componentes
necessários para a execução é variável, mas na estréia chegou efetivamente a
mais de mil músicos!) As sinfonias de <span class=SpellE>Mahler</span>,
contudo, não <span class=GramE>costumaram</span> usar o grande efetivo
orquestral como recurso de volume ou potência sonora, mas, principalmente, para
possibilitar grandes variedades de timbre  tendência que seria dominante no
século XX. Por isso, muitas vezes, assistir à execução de uma sinfonia de <span
class=SpellE>Mahler</span> é ver uma grande quantidade de músicos no palco, mas
quase nunca estão todos tocando juntos, sendo mais comum que uns poucos estejam
tocando e os demais estejam em pausa.</p>

<h3 style='text-align:justify'><span class=mw-headline>A orquestra no século XX</span></h3>

<p style='text-align:justify;text-indent:35.4pt'>Ao longo do século XX houve
uma tendência a abandonar a orquestra como meio privilegiado de expressão
musical dos compositores do ocidente, juntamente com o esgotamento criativo das
formas musicais tradicionalmente associadas à orquestra, especialmente a ópera,
a sinfonia e o concerto e o poema sinfônico. Cada vez que os compositores do
século XX voltaram à escrita orquestral e às <span class=GramE>sua formas</span>
tradicionais foi, normalmente, com o intuito de negar a tradição,
subvertendo-a. Ressalvas importantes podem ser feitas para um significativo
número de compositores que se manteve mais fiel à tradição do século XIX, como
os classificados de <a
href="http://pt.wikipedia.org/w/index.php?title=Nacionalistas&amp;action=edit&amp;redlink=1"
title="Nacionalistas (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>nacionalistas</span></a> e de <a
href="http://pt.wikipedia.org/w/index.php?title=Neo-cl%C3%A1ssicos&amp;action=edit&amp;redlink=1"
title="Neo-clássicos (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>neo-clássicos</span></a>. Mas mesmo
estes recriaram a tradição muito <span class=GramE>a seu</span> modo, usando
uma linguagem sinfônica peculiar pouco parecida com a dos compositores
novecentistas, especialmente quanto à linguagem harmônica e às combinações de
timbres, mesmo quando mantiveram o grupo orquestral em sua forma tradicional do
fim do século XIX.</p>

<p style='text-align:justify;text-indent:35.4pt'>A tendência ao abandono da
grande orquestra e de suas formas tradicionais pode ser comparada a uma crise
geral do período que ficou conhecido como <i style='mso-bidi-font-style:normal'><a
href="http://pt.wikipedia.org/wiki/Belle_%C3%A9poque" title="Belle époque"><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Belle</span></span><span style='color:windowtext;text-decoration:none;
text-underline:none'> <span class=SpellE>époque</span></span></a></i>. Muitos
analistas afirmam que foram os artistas os primeiros a sentirem e expressarem
esta crise do mundo burguês, que só ficou realmente patente com o estouro da <a
href="http://pt.wikipedia.org/wiki/1%C2%AA_Guerra_Mundial"
title="1ª Guerra Mundial"><span style='color:windowtext;text-decoration:none;
text-underline:none'>1ª Guerra Mundial</span></a> em 1914. De qualquer modo,
esta crise dos valores burgueses se fez sentir na escrita orquestral de várias
formas. Ficou mais difícil juntar grandes orquestras, o que levou a uma
tendência ao uso de pequenos grupos como na <i style='mso-bidi-font-style:normal'>Sinfonia
de Câmera</i> ou no <span class=SpellE><i style='mso-bidi-font-style:normal'>Pierrot</i></span><i
style='mso-bidi-font-style:normal'> <span class=SpellE>Lunaire</span></i> de <span
class=SpellE>Schoenberg</span>.</p>

<p style='text-align:justify;text-indent:35.4pt'>A dificuldade em juntar
grandes orquestras teve motivos econômicos, inclusive ligados à guerra e às
crises que a sucederam. Mas teve muito mais motivos estéticos: a escrita
orquestral foi se afastando do gosto do grande público por causa do abandono da
<span class=SpellE>discursividade</span> melódica, da harmonia tonal e da
regularidade rítmica pelos compositores. Sem a possibilidade de juntar grandes
públicos ficou mais difícil financiar grandes orquestras. A própria restrição
política imposta à música de vanguarda pelo nazismo e pelo stalinismo levou ao
exílio dos compositores radicais e à dificuldade de montar suas obras. Se eles
quisessem velas no palco, seria necessário apelar para grupos menores.</p>

<p style='text-align:justify;text-indent:35.4pt'>Por outros motivos, houve
também os países que não formaram o seu público tradicional de música
orquestral durante os séculos XVIII e XIX  caso em geral dos países do
continente americano. Estes não quiseram ou não puderam fazer os investimentos
necessários para isso no século XX, como fizeram os Estados Unidos e também os
países do leste europeu (especialmente após a 1945). Por isso compositores
latino-americanos também tiveram limitações para o uso de grandes orquestras,
mesmo quando continuavam usando técnicas de composição mais tradicionais.</p>

<p style='text-align:justify;text-indent:35.4pt'>Por estes motivos, pode-se
afirmar o fim da orquestra no século XX, pelo menos como grupo estável
utilizado pela maioria dos compositores. A orquestra transformou-se assim num
grupo instrumental dedicado à execução de música dos séculos XVIII e XIX, ou,
quando muito, de epígonos do século XX ou XXI. Os compositores passaram a
utilizar grupos <span class=GramE>menores e, ao mesmo tempo, maior</span>
variedade de instrumentos. A <a
href="http://pt.wikipedia.org/wiki/Orquestra%C3%A7%C3%A3o" title=Orquestração><span
style='color:windowtext;text-decoration:none;text-underline:none'>orquestração</span></a>
deixou de seguir um padrão mais ou menos aceito por grande conjunto de
compositores e tornou-se muito atomizada. Praticamente existe uma diferente
forma de orquestração para cada compositor ou, ainda mais, formas de
orquestração específicas para diferentes obras do mesmo compositor.</p>

<p style='text-align:justify;text-indent:35.4pt'>A grande característica da
orquestra do século XX (se é que se pode usar este termo) é o aumento da
presença dos instrumentos de percussão, que também ganham muito <st1:PersonName
ProductID="em variedade. Ao" w:st="on">em variedade. Ao</st1:PersonName>
contrário do que aconteceu nos séculos XVIII e XIX, quando os <span
class=SpellE>padrãos</span> de cordas e de sopros se padronizaram em torno de
um grupo definido formado por poucos instrumentos diferentes, a percussão orquestral
do século XX assumiu uma gama ilimitada de instrumentos, expendido a palheta
orquestral a níveis inimagináveis. Pode-se dizer que o aumento do uso e da
importância dos instrumentos de percussão na música do século XX acompanhou à
tendência geral de aumento da importância do fator timbre frente aos fatores
melodia e harmonia, bem como ao aumento do valor do ritmo. Obra paradigmática
deste aumento da importância da percussão foi <span class=GramE>a</span> obra <i
style='mso-bidi-font-style:normal'>Ionizações</i> de <a
href="http://pt.wikipedia.org/wiki/Edgar_Var%C3%A8se" title="Edgar Varèse"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Edgar <span
class=SpellE>Varèse</span></span></a>. Concluída em 1931, muitas vezes reputada
como a primeira obra escrita exclusivamente para instrumentos de percussão. Mas
em 1930 o compositor cubano <a
href="http://pt.wikipedia.org/w/index.php?title=Amadeo_Rold%C3%A1n&amp;action=edit&amp;redlink=1"
title="Amadeo Roldán (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>Amadeo <span class=SpellE>Roldán</span></span></a>
já tinha composto também para grupo de percussão as <i style='mso-bidi-font-style:
normal'>Rítmicas</i> <i style='mso-bidi-font-style:normal'>V</i> e <i
style='mso-bidi-font-style:normal'>VI</i>. A partir da segunda metade do
século, tornou-se bastante comum a escrita de obras para uma orquestra formada
unicamente por instrumentos de percussão.</p>

<p style='text-align:justify;text-indent:35.4pt'>Curioso observar que, mesmo o
abandono da orquestra como grupo definido e como principal meio expressivo, a
parte da composição destinada ao tratamento dos instrumentos musicais e de suas
combinações continua sendo denominada <a
href="http://pt.wikipedia.org/wiki/Orquestra%C3%A7%C3%A3o" title=Orquestração><span
style='color:windowtext;text-decoration:none;text-underline:none'>orquestração</span></a>.
Por isso não se pode deixar de mencionar que no século XX também houve uma
tendência pela valorização do ruído e pelo desenvolvimento de instrumentos
musicais não convencionais, bem como pelo seu uso em grupo  muitas vezes
mantendo o termo tradicional orquestra para designar instrumentos muito
diferentes daqueles consagrados na tradição clássico-romântica. Assim deve-se
registrar o surgimento de máquinas de som como o <a
href="http://pt.wikipedia.org/w/index.php?title=Th%C3%A9remin&amp;action=edit&amp;redlink=1"
title="Théremin (página não existe)"><span class=SpellE><span style='color:
windowtext;text-decoration:none;text-underline:none'>Théremin</span></span></a>,
os <i style='mso-bidi-font-style:normal'><a
href="http://pt.wikipedia.org/w/index.php?title=Intona-rumori&amp;action=edit&amp;redlink=1"
title="Intona-rumori (página não existe)"><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Intona-rumori</span></span></a></i>
ou máquinas de ruído do futurista italiano <a
href="http://pt.wikipedia.org/wiki/Luigi_Russolo" title="Luigi Russolo"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Luigi <span
class=SpellE>Russolo</span></span></a>, e o uso dos geradores elétricos ou
eletrônicos.</p>

<p style='text-align:justify'>Pioneiro da música eletrônica foi novamente Edgar
<span class=SpellE>Varése</span> com sua obra <i style='mso-bidi-font-style:
normal'>Poema Eletrônico</i>, de 1958. <a
href="http://pt.wikipedia.org/wiki/Pierre_Schaeffer" title="Pierre Schaeffer"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Pierre <span
class=SpellE>Schaeffer</span></span></a> desenvolveu o conceito da <a
href="http://pt.wikipedia.org/wiki/M%C3%BAsica_concreta" title="Música concreta"><span
style='color:windowtext;text-decoration:none;text-underline:none'>música
concreta</span></a>, com a gravação de sons em fita magnética e posterior
distorção em equipamentos elétricos. Estes conceitos acabaram misturando-se em
obras consideradas como <a
href="http://pt.wikipedia.org/w/index.php?title=M%C3%BAsica_eletro-ac%C3%BAstica&amp;action=edit&amp;redlink=1"
title="Música eletro-acústica (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>música <span class=SpellE>eletro-acústica</span></span></a>,
por misturar sons elétricos e instrumentos tradicionais. Trabalharam com esta
técnica os principais compositores hoje ainda vivos, como <a
href="http://pt.wikipedia.org/wiki/Berio" title=Berio><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Berio</span></span></a>,
<a href="http://pt.wikipedia.org/wiki/Stockhausen" title=Stockhausen><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Stockhausen</span></span></a> ou <a
href="http://pt.wikipedia.org/wiki/Penderecki" title=Penderecki><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Penderecki</span></span></a>. Uma divertida obra demonstrativa desta
técnica é <i style='mso-bidi-font-style:normal'>Santos <span class=SpellE>football</span>
<span class=SpellE>music</span></i> de <a
href="http://pt.wikipedia.org/wiki/Gilberto_Mendes" title="Gilberto Mendes"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Gilberto
Mendes</span></a>, para orquestra e fita magnética (cujo conteúdo é de
gravações de narrações de jogos de futebol feitas por <a
href="http://pt.wikipedia.org/wiki/Osmar_Santos" title="Osmar Santos"><span
style='color:windowtext;text-decoration:none;text-underline:none'>Osmar Santos</span></a>)
 obra composta na época em que <span class=GramE>o Santos</span>, time de
futebol da cidade do compositor, contava com a participação de Pelé, e chegava duas
vezes ao título mundial de futebol.</p>

