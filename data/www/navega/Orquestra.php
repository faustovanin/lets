<h2 style='text-align:justify'><span class=mw-headline>O regente</span></h2>

<p style='text-align:justify;text-indent:35.4pt'>No início da orquestra, ainda
não existia a figura do regente. Seja pelo tamanho reduzido dos grupos
orquestrais (normalmente não mais que 20 ou 30 músicos), seja pela menor
complexidade rítmica, normalmente não era necessária a regência, havendo apenas
um líder do grupo que orientasse os ensaios, ou mesmo que coordenasse a
execução a partir de seu próprio instrumento musical enquanto participava do
concerto.</p>

<p style='text-align:justify;text-indent:35.4pt'>Aponta-se o pioneirismo do
compositor <a href="http://pt.wikipedia.org/wiki/Lully" title=Lully><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Lully</span></span></a> (1632-1687), dirigente do famoso grupo dos <i
style='mso-bidi-font-style:normal'>24 violinos do rei</i>, na corte francesa de
meados do século XVII, que costumava marcar o pulso batendo no chão com um
pesado bastão. Além do inconveniente ruído que tal marcação ocasionava, esta
prática levou à morte do compositor, devido a uma gangrena causada após ele ter
atingido o próprio pé com o bastão durante a execução de uma obra.</p>

<p style='text-align:justify;text-indent:35.4pt'>Mas o fato é que somente pela
segunda metade do século XIX a figura do regente tornou-se comum. O aumento do
tamanho das orquestras e também da complexidade rítmica das obras executadas
levou ao fato de que <span class=GramE>tornava-se</span> praticamente
impossível executar certas obras sem o trabalho do regente.</p>

<p style='text-align:justify;text-indent:35.4pt'>Este <span class=GramE>torna-se</span>
responsável por decisões de interpretação como andamento, caráter, instrumento
ou voz a ser destacada em determinado trecho. Torna-se responsável também pela
coordenação dos ensaios, o que o obriga a conhecer previamente e muito bem a
totalidade da obra, para garantir a perfeita junção das partes de cada músico.
Finalmente, torna-se responsável pela marcação do tempo e das entradas mais
importantes durante a execução em concerto, função a mais aparente da atividade
de um maestro.</p>

