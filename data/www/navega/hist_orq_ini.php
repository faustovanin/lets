<h1>História da Orquestra</h1>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>A história da orquestra está ligada à história da <a
href="http://pt.wikipedia.org/wiki/M%C3%BAsica_instrumental"
title="Música instrumental"><span style='color:windowtext;text-decoration:none;
text-underline:none'>música instrumental</span></a>. Sempre existiu a prática
de música em instrumentos musicais, mas em geral esta prática foi sempre
minoritária em relação à música vocal. Na maioria das vezes, inclusive, os
instrumentos musicais eram usados como apoio ou acompanhamento às vozes.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Foi no século XVI, durante o <a
href="http://pt.wikipedia.org/wiki/Renascimento" title=Renascimento><span
style='color:windowtext;text-decoration:none;text-underline:none'>Renascimento</span></a>,
que a música instrumental começou a ser praticada de forma autônoma. Ou seja,
foi com a <b style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:
normal'><a href="javascript:popup('mus_ren.php')" border=0>música renascentista</a></i></b><i style='mso-bidi-font-style:normal'> </i>que
os instrumentos musicais ganharam o status de merecerem ser ouvidos
independentemente da música vocal. Inicialmente isto ocorreu com transcrições
de música vocal ou danças estilizadas, e os primeiros instrumentos solistas
foram o <a href="http://pt.wikipedia.org/wiki/%C3%93rg%C3%A3o" title=Órgão><span
style='color:windowtext;text-decoration:none;text-underline:none'>órgão</span></a>
e o <a href="http://pt.wikipedia.org/wiki/Ala%C3%BAde" title=Alaúde><span
style='color:windowtext;text-decoration:none;text-underline:none'>alaúde</span></a>.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>A idéia de formar <a
href="http://pt.wikipedia.org/w/index.php?title=Grupos_instrumentais&amp;action=edit&amp;redlink=1"
title="Grupos instrumentais (página não existe)"><span style='color:windowtext;
text-decoration:none;text-underline:none'>grupos instrumentais</span></a>
também já é muito antiga, mas durante o período medieval estes grupos não
tinham uma estrutura definida. É comum que as <b style='mso-bidi-font-weight:
normal'><i style='mso-bidi-font-style:normal'><a href="javascript:popup('part_conc.php')" border=0>partituras</a></i></b> desta época
sejam indicadas apenas para vozes, mas as partes vocais podiam ser reforçadas
ou mesmo substituídas por instrumentos musicais. É provável que o <span
class=SpellE><i style='mso-bidi-font-style:normal'>Hoquetus</i></span><i
style='mso-bidi-font-style:normal'> David</i>, obra musical do século XIV seja
a primeira composição instrumental. Não há indicação de instrumentos, mas os
estudiosos têm esta suspeita porque também não foi encontrado texto para o caso
de ser música vocal.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Além do apoio à música vocal, os grupos instrumentais também
foram, desde muito antigamente, usados em cerimônias públicas e em locais
abertos. As festas romanas no <a href="http://pt.wikipedia.org/wiki/Coliseu"
title=Coliseu><span style='color:windowtext;text-decoration:none;text-underline:
none'>Coliseu</span></a> já usavam grupos de <a
href="http://pt.wikipedia.org/wiki/Cornetas" title=Cornetas><span
style='color:windowtext;text-decoration:none;text-underline:none'>cornetas</span></a>
e outros instrumentos de grande volume. Acredita-se que por causa desta ligação
com festas pagãs e com espetáculos que envolviam o martírio de cristãos é que a
tradição cristã proibiu desde cedo o uso de instrumentos musicais na <b
style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:normal'><a href="javascript:popup('mus_lit.php')" border=0>música
litúrgica</a></i></b>. As cortes feudais também usaram com freqüência os
instrumentos mais estridentes para cerimônias de coroação e festas em lugares
abertos. Era comum chamar estes grupos instrumentais de música de estábulo.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

<p class=MsoNormal>Mas o surgimento da orquestra está ligado a uma autonomia e
uma padronização dos grupos instrumentais, seguindo sempre uma tendência de
desenvolvimento da cultura urbana e burguesa. Neste sentido, os primeiros
grupos a serem classificados como orquestras são aqueles determinados pelo
veneziano <span class=SpellE>Giovani</span> <a
href="http://pt.wikipedia.org/w/index.php?title=Gabrielli&amp;action=edit&amp;redlink=1"
title="Gabrielli (página não existe)"><span class=SpellE><span
style='color:windowtext;text-decoration:none;text-underline:none'>Gabrielli</span></span></a>
(1557-1612) para o acompanhamento de suas ’’Sinfonias Sacras’’ compostas por
volta de 1600. Quase simultaneamente, em Florença, Cláudio <a
href="http://pt.wikipedia.org/wiki/Monteverdi" title=Monteverdi><span
class=SpellE><span style='color:windowtext;text-decoration:none;text-underline:
none'>Monteverdi</span></span></a> (1567-1643) também define uma orquestra para
o acompanhamento de sua ópera <a href="http://pt.wikipedia.org/wiki/Orfeo"
title=Orfeo><span class=SpellE><span style='color:windowtext;text-decoration:
none;text-underline:none'>Orfeo</span></span></a>, composta em 1607. Estas
orquestras primitivas, surgidas no início do <a
href="http://pt.wikipedia.org/wiki/Per%C3%ADodo_barroco" title="Período barroco"><span
style='color:windowtext;text-decoration:none;text-underline:none'>período
barroco</span></a>, foram os primeiros grupos instrumentais com instrumentos
definidos, correspondendo às primeiras tentativas feitas por compositores em obter
um grupo instrumental de timbre definido. Ou seja, a partir deste período, os
compositores passaram a não mais deixar a definição do timbre dos grupos
instrumentais a cargo dos executantes, institucionalizando <span class=GramE>uma
certa</span> formação instrumental. Neste momento, ainda não havia uma fixação
da formação orquestral, o que só iria acontecer no fim do primeiro quartel do
século XVIII.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

