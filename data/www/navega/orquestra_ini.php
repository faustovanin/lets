<h1><span style='mso-fareast-font-family:"Times New Roman"'>Orquestra<o:p></o:p></span></h1>

<p class=MsoNormal style='text-align:justify'><o:p>&nbsp;</o:p></p>

<p class=MsoNormal><span style='mso-tab-count:1'>                </span>Uma
orquestra é um agrupamento instrumental utilizado sobretudo para a execução de <b
style='mso-bidi-font-weight:normal'><i style='mso-bidi-font-style:normal'><a href="javascript:popup('mus_eru.php')" border=0>música
erudita</a></i></b>. A pequenas orquestras dá-se o nome de orquestras de câmara<i
style='mso-bidi-font-style:normal'>.<o:p></o:p></i></p>

<p class=MsoNormal><i style='mso-bidi-font-style:normal'><span
style='mso-tab-count:1'>                </span></i>A orquestras completas dá-se
o nome de orquestras sinfônicas ou orquestras filarmônicas; embora estes
prefixos não especifiquem nenhuma diferença no que toca à constituição
instrumental ou ao papel da mesma, podem revelar-se úteis para distinguir
orquestras de uma mesma localidade. Na verdade, esses prefixos denotam a
maneira que é sustentada a orquestra. A orquestra filarmônica é sustentada por
uma instituição privada, ficando assim a sinfônica mantida por uma instituição
pública. Uma orquestra terá, tipicamente, mais de oitenta músicos, em alguns
casos mais de cem, embora em atuação esse número seja ajustado em função da
obra reproduzida. Em alguns casos, uma orquestra pode incluir músicos
contratados para tocar instrumentos específicos que não compõem o conjunto
oficial: por exemplo, nem todas as orquestras têm um harpista ou um
saxofonista.</p>

<p class=MsoNormal><o:p>&nbsp;</o:p></p>

